	// interrupt.
	void GameMove(void){
		int i,j;
		int	Anyalive = 0;
		
		
		// Move spaceship
		int slideX = Slide();
		if(slideX == Left && SpaceShip.x > 1){
			SpaceShip.x -= SpaceShip.vx;
		}
		else if (slideX == Right && SpaceShip.x < 119){
			SpaceShip.x += SpaceShip.vx;
		}
		
		
		
		
		// Move enemies down when hitting wall
		int bounce = 0;
		for(i=0; i < MaxRowEnemies; i++)
		{
			for (j=0; j<num_of_col; j++)
			{
			if(Enemy[i][j].life == alive){
				Enemy[i][j].needDraw = 1;
				int Anyalive = 1;

				if((Enemy[i][j].y > 140) || (Enemy[i][j].y < 10)){
					Enemy[i][j].life = dead;
				}				
				if((Enemy[i][1].x < 30) || (Enemy[i][1].x > 60)){
				Enemy[i][j].vx = -1*Enemy[i][j].vx;
					bounce += 1;
				}				
				Enemy[i][j].x += Enemy[i][j].vx;	
		}
			if (bounce == 2){
				bounce = 0;
				Enemy[i][j].y += Enemy[i][j].vy;	
	}
}
}		
		// Move Lasers
	bool SW_Pressed = Shoot_SW();
		if (SW_Pressed && Laser.life == dead)
	{
		Shoot();
	}

if(Laser.life == alive)
	{
		Laser.needDraw = 1;
		Anyalive = 1;

		if(Laser.y < 22 + Laser.h)
			Laser.life = dead;
		
		Laser.y += Laser.vy;
	}

	
			// Move Missiles
		for(i=0;i<3;i++){
			if(Missile.life == alive){
				Missile.needDraw = 1;
				Anyalive = 1;
								
				// Amer Qouneh's code for this structure is clearer than Valvano's.
				// Only one of those conditions is executed and control exits.
				if((Missile.y > 140) || Missile.y < 0){
						Missile.life = dead;
				}		
				
				if((Missile.x < 0) || Missile.x > 120){
					Missile.life = dead;
				}				
				Missile.x += Missile.vx;
				Missile.y += Missile.vy;
				
			}// if
		}// for		
		
	}// GameMove
	

	
	unsigned long FrameCount = 0;	// used for animation of a sprite.
	
	// Called in main 
	void GameDraw(void){
		int i,j;
		for(i=0; i<MaxRowEnemies; i++)
		{
			for (j=0; j<num_of_col; j++)
			{
			if(Enemy[i][j].needDraw)
				{
				if(Enemy[i][j].life == alive)
				{
					ST7735_DrawBitmap(Enemy[i][j].x, Enemy[i][j].y, Enemy[i][j].image[FrameCount], Enemy[i][j].w, Enemy[i][j].h);	// with animation for sprite
				}
				else
				{
					ST7735_DrawBitmap(Enemy[i][j].x, Enemy[i][j].y, Enemy[i][j].black, Enemy[i][j].w, Enemy[i][j].h);					
				}
				Enemy[i][j].needDraw = 0;	// clear the semaphore				
			}// if
		}// for
	}

		//Lasers
					if (Laser.needDraw)
					{
						if(Laser.life == alive)
						{
							ST7735_DrawBitmap(Laser.x, Laser.y, Laser.image[0], Laser.w, Laser.h);
						}
						else
						{
						ST7735_DrawBitmap(Laser.x, Laser.y, Laser.black, Laser.w, Laser.h);
					}
						
						Laser.needDraw = 0;
					}
		
		for(i=0;i<3;i++){
			if(Missile.needDraw){
				if(Missile.life == alive){
					ST7735_DrawBitmap(Missile.x, Missile.y, Missile.image[0], Missile.w, Missile.h);	// with animation for sprite
				}else{
					ST7735_DrawBitmap(Missile.x,Missile.y, Missile.black, Missile.w, Missile.h);					
				}
				Missile.needDraw = 0;	// clear the semaphore				
			}// if
		}// for
		

		// Draw spaceship
		ST7735_DrawBitmap(SpaceShip.x, SpaceShip.y, SpaceShip.image[FrameCount], SpaceShip.w, SpaceShip.h);	
		
		// Draw bunkers
		ST7735_DrawBitmap(55, 149, Bunker0, 18, 5);
		ST7735_DrawBitmap(20, 149, Bunker0, 18, 5);
		ST7735_DrawBitmap(90, 149, Bunker0, 18, 5);

		FrameCount = (FrameCount+1)&0x01; // 0,1,0,1,...
	}// GameDraw
	
	
	
